#371. Sum of Two Integers
#Given two integers a and b, return the sum of the two integers without using the operators + and -.

class Solution:
    def getSum(self, a: int, b: int) -> int:
        default_str = '*'
        print(a, b)

        if not a or not b:
            return 0

        # a_str = default_str * a
        print(len(default_str*b))

        return 0
